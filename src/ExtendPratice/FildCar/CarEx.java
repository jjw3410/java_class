package ExtendPratice.FildCar;

public class CarEx {

	public static void main(String[] args) {
		Car car=new Car();
		
		for(int i=0;i<10;++i) {
			
			int problemLocation=car.run();
			
			switch(problemLocation) {
			case 1:
				System.out.println("앞왼쪽 Hankook타이어로 교체");
				car.frontLeftTire=new HankookTire("앞왼쪽",15);
				break;
			case 2:
				System.out.println("앞오른쪽 kumho타이어로 교체");
				car.frontRightTire=new KumhoTire("앞오른쪽",13);
				break;
			case 3:
				System.out.println("뒤 왼쪽 Hankook타이어로 교체");
				car.backLeftTire=new HankookTire("뒤 왼쪽",14);
				break;
			case 4:
				System.out.println("뒤 오른쪽 kumho타이어로 교체");
				car.backRightTire=new KumhoTire("뒤 오른쪽",17);
				break;
			}
			System.out.println("---------------");
		}
		
		
		
		
		
		
		
		
	}
}
