package practice_Class;

public class Data {
	
	//멤버 필드 선언
	 int number;
	 String name;
	
	//각 멤버 필드의 관한 getter,setter
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int arg) {
		this.number=arg;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String arg) {
		this.name=arg;
	}
}
